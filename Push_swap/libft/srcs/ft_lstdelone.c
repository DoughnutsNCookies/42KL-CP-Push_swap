#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	t_list	*temp;

	if (!lst)
		return ;
	(*del)(lst->content);
	temp = lst->next;
	free(lst);
	lst = temp;
}
